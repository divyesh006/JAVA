public class Main {
    public static void main(String[] args)
    {
        int i=56;
        char ch='a';
        System.out.println(++ch);
        System.out.println(i++);
        System.out.println(i);
        System.out.println(++i);
        System.out.println(--i);
        System.out.println(i--);
    }
}